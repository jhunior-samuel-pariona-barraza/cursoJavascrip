//OPERADORES ARITMETICOS 
//Los operadores aritméticos realizan operaciones matemáticas, como sumas o restas con operandos.

//OPERADORES LOGICOS 
//Los operadores lógicos se usan para combinar dos valores Booleanos y devolver un resultado verdadero, falso o nulo. 

//OPERADORES DE COMPARACION
//Los operadores de comparación comparan dos expresiones y devuelven un valor Boolean que representa la relación de sus valores. 

//OPERADORES BINARIOS
//Existen algunos operadores que funcionan bit a bit, convirtiendo previamente los valores a binario. Son los siguientes: <<   >>   >>>   &   |   ^.

//OPERADORES DE ASIGNACION
//Los operadores de asignación devuelven el valor del objeto especificado por el operando izquierdo después de la asignación.


